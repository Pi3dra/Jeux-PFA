let char_to_block c =
  let box = ref Rect.{width = w_width; height = w_height} in
  let pos = 
    match c with
    (*Grass*)
    (*Upper*) 
    |"1 " -> Rect.{width = 1; height= 1}
    |"2 " -> Rect.{width = 3; height= 1}
    |"3 " -> Rect.{width = 5; height= 1}

    (*Middle*)
    |"4 " -> Rect.{width = 1; height= 3}
    |"6 " -> Rect.{width = 5; height= 3}

    (*Bottom*)
    |"7 " -> Rect.{width = 1; height= 5}
    |"8 " -> Rect.{width = 3; height= 5}
    |"9 " -> Rect.{width = 5; height= 5}

    (*Bricks*)
    (*Upper*) 
    |"11" -> Rect.{width = 15; height = 7}
    |"12" -> Rect.{width = 17; height = 7}
    |"13" -> Rect.{width = 19; height = 7}

    (*Middle*)
    |"14" -> Rect.{width = 15; height = 9}
    |"16" -> Rect.{width = 19; height = 9}

    (*bottom*)
    |"17" -> Rect.{width = 15; height = 11}
    |"18" -> Rect.{width = 17; height = 11}
    |"19" -> Rect.{width = 19; height = 11}

    (*Underground*)
    | "00" -> 
        let vectors = [Rect.{width = 3; height = 3}; 
                       Rect.{width = 3; height = 3};
                       Rect.{width = 7; height = 7}; 
                       Rect.{width = 5; height = 7}] in

        List.nth vectors (Random.int 4)

    (*Spikes*)
    | "21" -> Rect.{width = 1 ; height = 13}
    | "22" -> Rect.{width = 3 ; height = 13}
    | "23" -> Rect.{width = 5 ; height = 13}

    (*Transparent*)
    |"0 " -> Rect.{width = 0 ; height = 0}

    (*dirt*)
    |"10" -> Rect.{width = 7 ; height = 1}


    (*platforms en ordre : big left, small left, small right big right *)
    | "41" ->
      box := Rect.{width = 3*32 ; height = 32};
      Rect.{width = 9 ; height = 1}
    | "42" ->
      box := Rect.{width = 2*32 ; height = 32};
      Rect.{width = 10 ; height = 3}
    | "43" ->
      box := Rect.{width = 2*32 ; height = 32};
      Rect.{width = 7 ; height = 3}
    | "44" ->
      box := Rect.{width = 3*32 ; height = 32};
      Rect.{width = 7 ; height = 5}

    (*TODO add solid blocks and those who can be passed through below*)
    | "" | _ -> Rect.{width = 0;  height= 1}
  in 


  (*plateformes*)
  if c = "31" then
    (Anim.Img "platform.png", Rect.{width = 64 ; height = 32})
  (*boites*)
  else if c = "51" then
    (Anim.Img "crate.png", Rect.{width = 32 ; height = 32} )
  else if c = "52"then
    (Anim.Img "crate2.png", Rect.{width = 64 ; height = 64} )
  else
    (Anim.Tst ("tileset.png",Rect.k_mul 32 pos), !box)




